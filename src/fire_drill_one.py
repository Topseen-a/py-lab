first_number = int(input("Enter a number: "))
second_number = int(input("Enter another number: "))

first_number_squared = first_number * first_number
second_number_squared = second_number * second_number

sum_of_squares = first_number_squared + second_number_squared
difference_of_squares = first_number_squared - second_number_squared

print("The square of the first number is ", first_number_squared)
print("The square of the second number squared is ", second_number_squared)
print("The sum of the squares is ", sum_of_squares)
print("The difference of the squares is ", difference_of_squares)
